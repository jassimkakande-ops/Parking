const db = require('../../config/db');

/**
 * Finds a user in the public.users table by their ID.
 * @param {string} id - The UUID of the user.
 * @returns {Promise<object|null>} The user object or null.
 */
exports.findById = async (id) => {
  const query = `
    SELECT id, full_name, email, phone_number, role, is_active 
    FROM users 
    WHERE id = $1
  `;
  const { rows } = await db.query(query, [id]);
  return rows[0] || null;
};

exports.findByEmail = async (email) => {
  const query = `
    SELECT id, full_name, email, phone_number, role, is_active
    FROM users
    WHERE email = $1
  `;
  const { rows } = await db.query(query, [email]);
  return rows[0] || null;
};

/**
 * Creates a new user profile in the public.users table.
 * Ensure the ID passed here matches the ID generated by Supabase Auth.
 * @param {object} userData
 * @param {string} userData.id
 * @param {string} userData.full_name
 * @param {string} userData.email
 * @param {string} userData.phone_number
 * @param {string} userData.role
 * @returns {Promise<object>} The created user object.
 */
exports.createUserProfile = async ({ id, full_name, email, phone_number, role }) => {
  const query = `
    INSERT INTO users (id, full_name, email, phone_number, role)
    VALUES ($1, $2, $3, $4, $5)
    RETURNING id, full_name, email, phone_number, role, is_active
  `;
  const values = [id, full_name, email, phone_number, role];
  const { rows } = await db.query(query, values);
  return rows[0];
};

/**
 * Checks if a user profile exists by email or phone.
 * @param {string} email 
 * @param {string} phone_number 
 * @returns {Promise<boolean>} True if exists, false otherwise.
 */
exports.existsByEmailOrPhone = async (email, phone_number) => {
  const query = `SELECT 1 FROM users WHERE email = $1 OR phone_number = $2 LIMIT 1`;
  const { rows } = await db.query(query, [email, phone_number]);
  return rows.length > 0;
};

/**
 * Retrieves all users (for admin).
 * @returns {Promise<Array>} List of users
 */
exports.findAllUsers = async () => {
  const query = `
    SELECT id, full_name, email, phone_number, role, is_active, created_at
    FROM users
    ORDER BY created_at DESC
  `;
  const { rows } = await db.query(query);
  return rows;
};

/**
 * Updates a user's is_active status (soft delete or activate).
 * @param {string} id 
 * @param {boolean} isActive 
 * @returns {Promise<object|null>}
 */
exports.updateUserStatus = async (id, isActive) => {
  const query = `
    UPDATE users 
    SET is_active = $1 
    WHERE id = $2 
    RETURNING id, full_name, email, phone_number, role, is_active
  `;
  const { rows } = await db.query(query, [isActive, id]);
  return rows[0] || null;
};

/**
 * Retrieves all unique drivers who have booked at a specific facility.
 * Returns driver details along with total bookings and total amount spent.
 */
exports.findDriversByFacility = async (facilityId) => {
  const query = `
    SELECT 
      u.id, 
      u.full_name, 
      u.email, 
      u.phone_number, 
      COUNT(b.id) as total_bookings,
      MAX(b.created_at) as last_booking_date
    FROM users u
    JOIN bookings b ON u.id = b.driver_id
    WHERE b.facility_id = $1
    GROUP BY u.id
    ORDER BY last_booking_date DESC
  `;
  const { rows } = await db.query(query, [facilityId]);
  return rows;
};

/**
 * Hard deletes a user from the public.users table.
 * Due to ON DELETE CASCADE constraints, this will also delete their facilities, bookings, slots, and payments.
 * @param {string} id - The UUID of the user.
 */
exports.deleteUser = async (id) => {
  const query = `DELETE FROM users WHERE id = $1`;
  await db.query(query, [id]);
};

/**
 * Updates a user's role.
 * @param {string} id - The UUID of the user.
 * @param {string} role - The new role (driver, owner, admin).
 * @returns {Promise<object|null>} The updated user object.
 */
exports.updateUserRole = async (id, role) => {
  const query = `
    UPDATE users 
    SET role = $1 
    WHERE id = $2 
    RETURNING id, full_name, email, phone_number, role, is_active
  `;
  const { rows } = await db.query(query, [role, id]);
  return rows[0] || null;
};
